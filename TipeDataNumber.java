public class TipeDataNumber {
    public static void main(String[] args) {
        //bilangan bulat
        byte tipeDataByte = 10;
        short tipeDataShort = 3276;
        int tipeDataInt = 10;
        long tipeDataLong = 10L;

        //bilangan pecahan
        float tipeDataFloat = 4.4F;
        double tipeDataDouble = 5.5;

        //sistem bilangan
        int decimalInt = 20;
        int hexDecimal = 0xFF;
        int binDecimal = 0b0101;

        int sebuahbilangan = 1_000_000_000;

        System.out.println(sebuahbilangan);
    }
}
